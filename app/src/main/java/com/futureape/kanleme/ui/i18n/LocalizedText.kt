@Composable
fun Text(
    text: String,
    modifier: Modifier = Modifier,
    color: Color = Color.Unspecified,
    fontSize: TextUnit = TextUnit.Unspecified,
    fontStyle: FontStyle? = null,
    fontWeight: FontWeight? = null,
    fontFamily: FontFamily? = null,
    letterSpacing: TextUnit = TextUnit.Unspecified,
    textDecoration: TextDecoration? = null,
    textAlign: TextAlign? = null,
    lineHeight: TextUnit = TextUnit.Unspecified,
    overflow: TextOverflow = TextOverflow.Clip,
    softWrap: Boolean = true,
    maxLines: Int = Int.MAX_VALUE,
    minLines: Int = 1,
    onTextLayout: (TextLayoutResult) -> Unit = {},
    style: TextStyle = LocalTextStyle.current,
) {
    val localeTag = LocalConfiguration.current.locales[0]?.toLanguageTag().orEmpty()
    val localized = remember(text, localeTag) { UiTextLocalizer.localize(text, localeTag) }
    androidx.compose.material3.Text(
        text = localized,
        modifier = modifier,
        color = color,
        fontSize = fontSize,
        fontStyle = fontStyle,
        fontWeight = fontWeight,
        fontFamily = fontFamily,
        letterSpacing = letterSpacing,
        textDecoration = textDecoration,
        textAlign = textAlign,
        lineHeight = lineHeight,
        overflow = overflow,
        softWrap = softWrap,
        maxLines = maxLines,
        minLines = minLines,
        onTextLayout = onTextLayout,
        style = style,
    )
}

object UiTextLocalizer {
    fun localize(text: String, localeTag: String): String {
        val tag = localeTag.lowercase()
        return when {
            tag.startsWith("en") -> toEnglish(text)
            tag.contains("hant") || tag.startsWith("zh-tw") || tag.startsWith("zh-hk") || tag.startsWith("zh-mo") -> toTraditional(text)
            else -> text.replace("回留", "看了么")
        }
    }

    private fun toEnglish(text: String): String {
        if (!text.hasHan()) return text
        enExact[text]?.let { return it }
        enPatterns.forEach { (regex, transform) ->
            regex.matchEntire(text)?.let {
                val transformed = transform(it)
                return if (transformed.hasHan()) englishFallback(text, transformed) else transformed
            }
        }
        var result = text.replace("看了么", "Keepix").replace("回留", "Keepix")
        enPhrases.forEach { (from, to) -> result = result.replace(from, to) }
        result = result
            .replace("（", " (")
            .replace("）", ")")
            .replace("，", ", ")
            .replace("。", ".")
            .replace("；", "; ")
            .replace("：", ": ")
            .replace("、", ", ")
            .replace("·", "·")
            .replace(Regex("\\s+"), " ")
            .trim()
        return if (result.hasHan()) englishFallback(text, result) else result
    }

    private fun englishFallback(original: String, partiallyLocalized: String): String {
        val base = when {
            original.contains("隐私") || original.contains("資料") || original.contains("数据") -> "Privacy details"
            original.contains("更新") || original.contains("修复") || original.startsWith("v") -> "Release note"
            original.contains("帮助") || original.contains("教程") || original.contains("引导") || original.contains("说明") -> "Help"
            original.contains("权限") || original.contains("授权") || original.contains("访问") -> "Permission details"
            original.contains("回收") || original.contains("待删") || original.contains("删除") || original.contains("永久") -> "Trash details"
            original.contains("文件夹") || original.contains("路径") || original.contains("归档") || original.contains("移动") -> "Folder details"
            original.contains("相似") || original.contains("重复") || original.contains("连拍") || original.contains("模糊") -> "Similar photo details"
            original.contains("收藏") -> "Favorite details"
            original.contains("视频") && (original.contains("照片") || original.contains("图片")) -> "Media details"
            original.contains("视频") -> "Video details"
            original.contains("照片") || original.contains("图片") || original.contains("相册") -> "Photo details"
            original.contains("整理") || original.contains("清理") || original.contains("释放") -> "Cleaning details"
            original.contains("设置") || original.contains("显示") || original.contains("主题") -> "Settings details"
            else -> "Details"
        }
        val hints = Regex("""[A-Za-z0-9][A-Za-z0-9._/%:-]*""")
            .findAll(partiallyLocalized)
            .map { it.value }
            .filterNot { it.equals("ID", ignoreCase = true) || it.length > 24 }
            .distinct()
            .take(3)
            .toList()
        return if (hints.isEmpty()) base else base + " · " + hints.joinToString(" / ")
    }

    private fun toTraditional(text: String): String {
        if (!text.hasHan()) return text
        var result = text.replace("回留", "看了麼")
        zhHantPhrases.forEach { (from, to) -> result = result.replace(from, to) }
        return result.map { zhHantChars[it] ?: it }.joinToString("")
    }

